package fr.openwide.alfresco.query.repo.exception;


public class RemoteArgumentException extends AbstractRemoteException {

	private static final long serialVersionUID = -5216197872991452353L;

	public RemoteArgumentException() {
		super();
	}

	public RemoteArgumentException(String message, Throwable cause) {
		super(message, cause);
	}

	public RemoteArgumentException(String message) {
		super(message);
	}

	public RemoteArgumentException(Throwable cause) {
		super(cause);
	}

}
